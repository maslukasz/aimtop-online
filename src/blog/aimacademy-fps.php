        <h2 class="text-[#1aa9bf] text-2xl mb-6 ml-5 mt-5">Training Techniques</h2>
        <h3 class="text-[#009a43] text-xl mb-3 ml-3">Flick Shots</h3>
        <p class="mb-8">Flick shots involve quickly and precisely aiming at a target that appears suddenly. Practicing
            this
            technique requires fast mouse movements and accurate targeting. This can be trained by setting dynamic
            targets in
            training programs or in game modes with bots.</p>

        <h3 class="text-[#009a43] text-xl mb-3 ml-3">Tracking</h3>
        <p class="mb-8">Tracking is a technique that involves following a moving target and keeping the crosshair on it.
            This is
            particularly useful in games with dynamic opponents, such as "Overwatch" or "Apex Legends". Tracking can be
            practiced in special modes in games or in training applications.</p>

        <h3 class="text-[#009a43] text-xl mb-3 ml-3">Micro Adjustments</h3>
        <p class="mb-8">Micro adjustments are small, precise mouse movements that help in accurate targeting of small
            targets or
            in situations requiring high precision. Practicing this technique involves regularly performing small mouse
            movements, both in games and in training programs.</p>

